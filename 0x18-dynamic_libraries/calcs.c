#include "calc.h"

/**
  * add - function to add numbers.
  * @a: As the first number.
  * @b: as the second number.
  *
  * Return: Always 1 on success.
  */

int add(int a, int b)
{
	return (a + b);
}

/**
  * sub - function to subtract numbers.
  * @a: As the first number.
  * @b: as the second number.
  *
  * Return: Always 1 on success.
  */

int sub(int a, int b)
{
	return (a - b);
}

/**
  * mul - function to multiply numbers.
  * @a: As the first number.
  * @b: as the second number.
  *
  * Return: Always 1 on success.
  */

int mul(int a, int b)
{
	return (a * b);
}

/**
  * div - function to divide numbers.
  * @a: As the first number.
  * @b: as the second number.
  *
  * Return: Always 1 on success.
  */

int div(int a, int b)
{
	return (a / b);
}

/**
  * mod - function to find riminder of numbers.
  * @a: As the first number.
  * @b: as the second number.
  *
  * Return: Always 1 on success.
  */

int mod(int a, int b)
{
	return (a % b);
}
